def deep_merge(base, additions):
    """Merge additions into base dict without overwriting existing keys."""
    for key, value in additions.items():
        if key not in base:
            base[key] = value
        elif isinstance(base[key], dict) and isinstance(value, dict):
            deep_merge(base[key], value)
    return base


def process_locale(locale, additions):
    filepath = os.path.join(BASE, f"{locale}.json")
    with open(filepath, "r", encoding="utf-8") as f:
        data = json.load(f)

    for namespace, keys in additions.items():
        # Handle special namespaces that merge into existing ones
        if namespace == "company_extra":
            if "company" not in data:
                data["company"] = {}
            deep_merge(data["company"], keys)
        elif namespace == "jobAlerts_extra":
            if "jobAlerts" not in data:
                data["jobAlerts"] = {}
            deep_merge(data["jobAlerts"], keys)
        else:
            if namespace not in data:
                data[namespace] = {}
            deep_merge(data[namespace], keys)

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")

    print(f"✅ {locale}.json mis à jour")


